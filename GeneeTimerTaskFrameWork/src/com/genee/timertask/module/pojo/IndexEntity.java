public class IndexEntity {
	
	private int indexId;
	
	private String indexName;
	
	private String indexCode;
	
	private String indexFlag;
	
	private int indexSort;
	
	private String indexOnOff;
	
	private int indexType;
	
	public int getIndexId() {
		return indexId;
	}

	public void setIndexId(int indexId) {
		this.indexId = indexId;
	}

	public String getIndexName() {
		return indexName;
	}

	public void setIndexName(String indexName) {
		this.indexName = indexName;
	}

	public String getIndexCode() {
		return indexCode;
	}

	public void setIndexCode(String indexCode) {
		this.indexCode = indexCode;
	}

	public String getIndexFlag() {
		return indexFlag;
	}

	public void setIndexFlag(String indexFlag) {
		this.indexFlag = indexFlag;
	}

	public int getIndexSort() {
		return indexSort;
	}

	public void setIndexSort(int indexSort) {
		this.indexSort = indexSort;
	}

	public String getIndexOnOff() {
		return indexOnOff;
	}

	public void setIndexOnOff(String indexOnOff) {
		this.indexOnOff = indexOnOff;
	}

	public int getIndexType() {
		return indexType;
	}

	public void setIndexType(int indexType) {
		this.indexType = indexType;
	}
	
}
